"""Escreva uma função que receba dois números inteiros e retorne a média aritmética dos mesmos.

Input
A função recebe como parâmetros 2 (dois) números inteiros a e b (1 ≤ a, b ≤ 105).

Assinatura da função em Python:

def media(a,b)

Importante: O código que você enviar não deve mostrar nada na tela (função print()). Deve conter apenas a implementação da função.

Output
A função deve retornar um valor inteiro, com a média aritmética entre a e b."""

def media(a,b):
    m = (a+b)/2
    return int(m)
